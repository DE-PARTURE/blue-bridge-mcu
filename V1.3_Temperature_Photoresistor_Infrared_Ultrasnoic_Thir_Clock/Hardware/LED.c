#include "Sys.h"
/**************************************************************************
변鑒묘콘：듐좋데몸LED
흙왯꽝鑒：LED乞多，0槨홍췻
럿쀼令  ：轟
**************************************************************************/
void LEDSingleRun(u8 index){
	
	EA = 0;
	
	P2 = (P2 & 0x1F) | 0x80;
	if(index == 0){
		P0 = 0xFF;
	}else{
		P0 = _crol_(0xFE,(index - 1));// 1111 1110
	}
	
	P2 = P2 & 0x1F;
	
	EA = 1;
}
