/**************************************************************************
函数功能：温度数据解析
入口参数：无
返回值  ：无
**************************************************************************/

/*******************************/
void Temp_Get()
{
	bit ack;
	u8 LSB , MSB;
	u8 index;
	u32 temp;
	ack = InitDS18B20();
	if(!ack)
	{
		Write_DS18B20(0xCC);							//跳过ROM设置
		Write_DS18B20(0xBE);							//读取暂存器指令
		LSB = Read_DS18B20();							//读取低八位
		MSB = Read_DS18B20();							//读取高八位
		temp = ( ((u32)MSB) << 8) + LSB;	//数据组合
		temp *= 625;											//将DS18B20原始数据转化为温度数据
		//temp >>= 4;											//清除小数位
		for(index = 0; index < 7; index ++)
		{//清空数码管缓冲区
			SMG_Buff[index] = 0xFF;
		}
		//分离数位
		SMG_Buff[1] = SMG[temp / 100000];	
		SMG_Buff[2] = SMG[(temp / 10000) % 10] & 0x7F;
		SMG_Buff[3] = SMG[(temp / 1000) % 10];
		SMG_Buff[4] = SMG[(temp / 100) % 10];
		SMG_Buff[5] = SMG[(temp / 10) % 10];
		SMG_Buff[6] =	SMG[temp % 10];
		SMG_Buff[7] = 0xC6;								//C
	}
	Start_DS18B20();
}
