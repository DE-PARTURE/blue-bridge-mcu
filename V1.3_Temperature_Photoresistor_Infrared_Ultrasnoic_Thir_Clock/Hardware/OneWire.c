#include "Sys.h"

sbit DQ = P1^4;  //单总线接口

//单总线延时函数
void Delay_OneWire(unsigned int t)  //IAP15F2K61S2
{
	/*******************************/
	t = t * 7;
	/*******************************/
	while(t--);
}

//通过单总线向DS18B20写一个字节
void Write_DS18B20(unsigned char dat)
{
	unsigned char i;
	/*******************************/
	EA = 0;
	/*******************************/
	for(i=0;i<8;i++)
	{
		DQ = 0;
		DQ = dat&0x01;
		Delay_OneWire(5);
		DQ = 1;
		dat >>= 1;
	}
	Delay_OneWire(5);
	/*******************************/
	EA = 1;
	/*******************************/
}

//从DS18B20读取一个字节
unsigned char Read_DS18B20(void)
{
	unsigned char i;
	unsigned char dat;
	/*******************************/
  EA = 0;
	/*******************************/
	
	for(i=0;i<8;i++)
	{
		DQ = 0;
		dat >>= 1;
		DQ = 1;
		if(DQ)
		{
			dat |= 0x80;
		}	    
		Delay_OneWire(5);
	}
	/*******************************/
	EA = 1;
	/*******************************/
	return dat;
}
/**************************************************************************
函数功能：DS18B20设备初始化,发送检测存在脉冲
入口参数：无
返回值  ：0存在DS18B20,1不存在DS18B20
**************************************************************************/
bit InitDS18B20(void)
{
  	bit initflag = 0;
  	
  	DQ = 1;
  	Delay_OneWire(12);
  	DQ = 0;
  	Delay_OneWire(80);
  	DQ = 1;
  	Delay_OneWire(10); 
    initflag = DQ;     
  	Delay_OneWire(5);
	
	
  	return initflag;
}
/**************************************************************************
函数功能：DS18B20设备启动(温度转换)
入口参数：无
返回值  ：无
**************************************************************************/

/*******************************/
bit Start_DS18B20()
{
	bit ack;
	ack = InitDS18B20();
	if(!ack)
	{
		Write_DS18B20(0xCC);//跳过片选直接控制总线上的所有器件
		Write_DS18B20(0x44);//温度转换指令
	}
	return ~ack;
}
